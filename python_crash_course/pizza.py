def make_pizza(size, *toppings):
    """Summarize the pizza we are about to make."""
    print(f"\nMaking a {size}-inch pizza with the following toppings:")
    for topping in toppings:
        print(f"- {topping}")
def subtract(a, b):
    """Subtract two numbers and return the result."""
    return a - b
def add(a, b):
    """add numbers"""
    return a + b
def multiply(a, b):
    """multiply numbers"""
    return a * b